-- Change coupon capacity from redemption-time competition to claim-time
-- allocation. Backend/API behavior will be added separately.

CREATE TYPE public.coupon_claim_status AS ENUM (
  'claimed',
  'consumed',
  'revoked',
  'expired'
);

ALTER TABLE public.coupons
  DROP CONSTRAINT ck_coupons_consumed_quantity;

ALTER TABLE public.coupons
  RENAME COLUMN consumed_quantity TO claimed_quantity;

ALTER TABLE public.coupons
  ADD CONSTRAINT ck_coupons_claimed_quantity CHECK (
    claimed_quantity >= 0
    AND claimed_quantity <= issue_quantity
  );

COMMENT ON COLUMN public.coupons.claimed_quantity IS
  'Atomic claim allocation counter; cannot exceed issue_quantity.';

CREATE TABLE public.coupon_claims (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  coupon_id BIGINT NOT NULL,
  customer_id VARCHAR(32) NOT NULL,
  status public.coupon_claim_status NOT NULL DEFAULT 'claimed',
  claimed_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  consumed_at TIMESTAMPTZ,
  revoked_at TIMESTAMPTZ,
  CONSTRAINT pk_coupon_claims PRIMARY KEY (id),
  CONSTRAINT fk_coupon_claims_coupon_id
    FOREIGN KEY (coupon_id) REFERENCES public.coupons(id)
    ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT fk_coupon_claims_customer_id
    FOREIGN KEY (customer_id) REFERENCES public.customers(id)
    ON UPDATE CASCADE ON DELETE RESTRICT,
  CONSTRAINT uq_coupon_claims_coupon_customer
    UNIQUE (coupon_id, customer_id),
  CONSTRAINT ck_coupon_claims_timestamps CHECK (
    (status = 'claimed' AND consumed_at IS NULL AND revoked_at IS NULL)
    OR (status = 'consumed' AND consumed_at IS NOT NULL AND revoked_at IS NULL)
    OR (status IN ('revoked', 'expired') AND revoked_at IS NOT NULL)
  ),
  CONSTRAINT ck_coupon_claims_time_order CHECK (
    (consumed_at IS NULL OR consumed_at >= claimed_at)
    AND (revoked_at IS NULL OR revoked_at >= claimed_at)
  )
);

CREATE INDEX idx_coupon_claims_customer_status
  ON public.coupon_claims (customer_id, status);

CREATE INDEX idx_coupon_claims_coupon_status
  ON public.coupon_claims (coupon_id, status);

