    HAVING count(*) > 1
  ) THEN
    RAISE EXCEPTION
      'V738 guard: one customer used the same coupon more than once';
  END IF;
END
$$;

INSERT INTO public.coupon_claims (
  coupon_id,
  customer_id,
  status,
  claimed_at,
  consumed_at
)
SELECT usage.coupon_id,
       orders.customer_id,
       CASE
         WHEN orders.payment_status = 'paid'::public.payment_status
          AND orders.status IN ('unshipped', 'shipped', 'completed')
           THEN 'consumed'::public.coupon_claim_status
         ELSE 'claimed'::public.coupon_claim_status
       END,
       usage.applied_at,
       CASE
         WHEN orders.payment_status = 'paid'::public.payment_status
          AND orders.status IN ('unshipped', 'shipped', 'completed')
           THEN COALESCE(orders.paid_at, usage.applied_at)
         ELSE NULL
       END
FROM public.order_coupons usage
JOIN public.orders orders ON orders.id = usage.order_id
WHERE usage.coupon_id IS NOT NULL;

