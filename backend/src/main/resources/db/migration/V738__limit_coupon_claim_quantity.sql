UPDATE public.coupons coupon
SET claimed_quantity = counts.quantity,
    updated_at = now()
FROM (
  SELECT claim.coupon_id, count(*)::INTEGER AS quantity
  FROM public.coupon_claims claim
  WHERE claim.status IN ('claimed', 'consumed')
  GROUP BY claim.coupon_id
) counts
WHERE counts.coupon_id = coupon.id;

UPDATE public.coupons coupon
SET claimed_quantity = 0,
    updated_at = now()
WHERE NOT EXISTS (
  SELECT 1
  FROM public.coupon_claims claim
  WHERE claim.coupon_id = coupon.id
    AND claim.status IN ('claimed', 'consumed')
);

DO $$
BEGIN
  IF EXISTS (
    SELECT 1
    FROM public.coupons
    WHERE claimed_quantity > issue_quantity
  ) THEN
    RAISE EXCEPTION
      'V738 guard: existing claims exceed coupon issue_quantity';
  END IF;
END
$$;

CREATE FUNCTION public.allocate_coupon_claim_capacity()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  IF NEW.status IN ('claimed', 'consumed') THEN
    UPDATE public.coupons
    SET claimed_quantity = claimed_quantity + 1,
        updated_at = now()
    WHERE id = NEW.coupon_id
      AND status = 'active'::public.coupon_status
      AND now() >= valid_from
      AND now() < valid_until
      AND claimed_quantity < issue_quantity;

    IF NOT FOUND THEN
      RAISE EXCEPTION
        'Coupon % is unavailable, expired, or fully claimed', NEW.coupon_id
        USING ERRCODE = 'check_violation';
    END IF;
  END IF;

  RETURN NEW;
END
$$;

CREATE FUNCTION public.sync_coupon_claim_capacity()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  old_allocated BOOLEAN;
  new_allocated BOOLEAN;
BEGIN
  IF TG_OP = 'DELETE' THEN
    IF OLD.status IN ('claimed', 'consumed') THEN
      UPDATE public.coupons
      SET claimed_quantity = claimed_quantity - 1,
          updated_at = now()
      WHERE id = OLD.coupon_id
        AND claimed_quantity > 0;

      IF NOT FOUND THEN
        RAISE EXCEPTION
          'Coupon % claim counter cannot be released', OLD.coupon_id
          USING ERRCODE = 'check_violation';
      END IF;
    END IF;

    RETURN OLD;
  END IF;

  IF NEW.coupon_id <> OLD.coupon_id THEN
    RAISE EXCEPTION 'coupon_claims.coupon_id is immutable'
      USING ERRCODE = 'check_violation';
  END IF;

  old_allocated := OLD.status IN ('claimed', 'consumed');
  new_allocated := NEW.status IN ('claimed', 'consumed');

  IF old_allocated AND NOT new_allocated THEN
    UPDATE public.coupons
    SET claimed_quantity = claimed_quantity - 1,
        updated_at = now()
    WHERE id = OLD.coupon_id
      AND claimed_quantity > 0;

    IF NOT FOUND THEN
      RAISE EXCEPTION
        'Coupon % claim counter cannot be released', OLD.coupon_id
        USING ERRCODE = 'check_violation';
    END IF;
  ELSIF NOT old_allocated AND new_allocated THEN
    UPDATE public.coupons
    SET claimed_quantity = claimed_quantity + 1,
        updated_at = now()
    WHERE id = NEW.coupon_id
      AND status = 'active'::public.coupon_status
      AND now() >= valid_from
      AND now() < valid_until
      AND claimed_quantity < issue_quantity;

    IF NOT FOUND THEN
      RAISE EXCEPTION
        'Coupon % is unavailable, expired, or fully claimed', NEW.coupon_id
        USING ERRCODE = 'check_violation';
    END IF;
  END IF;

  RETURN NEW;
END
$$;

CREATE TRIGGER trg_coupon_claims_allocate_capacity
BEFORE INSERT ON public.coupon_claims
FOR EACH ROW
EXECUTE FUNCTION public.allocate_coupon_claim_capacity();

CREATE TRIGGER trg_coupon_claims_sync_capacity_on_update
BEFORE UPDATE ON public.coupon_claims
FOR EACH ROW
EXECUTE FUNCTION public.sync_coupon_claim_capacity();

CREATE TRIGGER trg_coupon_claims_sync_capacity_on_delete
BEFORE DELETE ON public.coupon_claims
FOR EACH ROW
EXECUTE FUNCTION public.sync_coupon_claim_capacity();

DROP VIEW public.coupon_usage_stats;

CREATE VIEW public.coupon_claim_stats AS
SELECT coupon.id AS coupon_id,
       coupon.issue_quantity,
       count(claim.id) FILTER (
         WHERE claim.status IN ('claimed', 'consumed')
       )::BIGINT AS claimed_quantity,
       count(claim.id) FILTER (
         WHERE claim.status = 'consumed'
       )::BIGINT AS consumed_quantity,
       (
         coupon.issue_quantity
         - count(claim.id) FILTER (
             WHERE claim.status IN ('claimed', 'consumed')
           )
       )::BIGINT AS remaining_claimable_quantity
FROM public.coupons coupon
LEFT JOIN public.coupon_claims claim ON claim.coupon_id = coupon.id
GROUP BY coupon.id, coupon.issue_quantity;

COMMENT ON VIEW public.coupon_claim_stats IS
  'Coupon issue capacity allocated at claim time; the 51st claim of a 50-quantity coupon must fail.';

COMMENT ON TABLE public.coupon_usage_adjustments IS
  'Legacy redemption adjustment ledger retained for audit; new capacity authority is coupon_claims.';
